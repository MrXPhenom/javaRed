package L1_StarterLevel.C2_Operations;

public class C5_Comparison {
    public static void main(String[] args){
        byte val1 = 0, val2 = 1;
        boolean res;

        res = val1 < val2;
        System.out.println(res);

        res = val1 > val2;
        System.out.println(res);

        res = val1 <= val2;
        System.out.println(res);

        res = val1 >= val2;
        System.out.println(res);

        res = val1 == val2;
        System.out.println(res);

        res = val1 != val2;
        System.out.println(res);
    }
}
